export const getProductById = (id: string): Product | undefined => {
  return PRODUCTS.find((p) => p.id === id)
}

export const getProductsByCategory = (categoryId: string): Product[] => {
  return PRODUCTS.filter((p) => p.categoryId === categoryId)
}

export const getCategoryById = (id: string): ProductCategory | undefined => {
  return PRODUCT_CATEGORIES.find((c) => c.id === id)
}

export const getLowStockProducts = (): Product[] => {
  return PRODUCTS.filter(
    (p) =>
      p.trackStock &&
      p.currentStock !== undefined &&
      p.lowStockThreshold !== undefined &&
      p.currentStock < p.lowStockThreshold &&
      p.currentStock > 0
  )
}

export const getOutOfStockProducts = (): Product[] => {
  return PRODUCTS.filter(
    (p) => p.trackStock && p.currentStock === 0
  )
}
